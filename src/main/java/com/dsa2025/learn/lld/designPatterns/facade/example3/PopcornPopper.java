package com.dsa2025.learn.lld.designPatterns.facade.example3;

public class PopcornPopper {

    public void on() {
        System.out.println("Popcorn Popper is ON");
    }

    public void pop() {
        System.out.println("Popping popcorn...");
    }

    public void off() {
        System.out.println("Popcorn Popper is OFF");
    }

}
